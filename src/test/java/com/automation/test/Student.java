package com.automation.test;

public class Student {
    String firstName;
    String lastName;
    String username;
    String email;
    String password;
    String phone;
    String dateOfBirth;

    public Student(String firstName, String lastName, String username, String email, String password, String phone, String dateOfBirth) {
        this.firstName = firstName;
        this.lastName = lastName;
        this.username = username;
        this.email = email;
        this.password = password;
        this.phone = phone;
        this.dateOfBirth = dateOfBirth;
    }
}
